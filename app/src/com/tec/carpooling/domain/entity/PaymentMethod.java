package com.tec.carpooling.domain.entity;

/**
 *
 * @author hidal
 */
public class PaymentMethod {
    private long id;
    private String name;

    public PaymentMethod(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() { return id; }
    public String getName() { return name; }

    @Override
    public String toString() {
        return name; // This is what will show in the combo box
    }
}
